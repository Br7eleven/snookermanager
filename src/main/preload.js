const { contextBridge, ipcRenderer } = require('electron');

contextBridge.exposeInMainWorld('electron', {
  invoke: (channel, data) => ipcRenderer.invoke(channel, data),
  login: (credentials) => ipcRenderer.invoke('auth:login', credentials),
  logout: () => ipcRenderer.invoke('auth:logout'),
  rememberUsername: (username) => ipcRenderer.invoke('auth:rememberUsername', username),
  forgetUsername: () => ipcRenderer.invoke('auth:forgetUsername'),
  getRememberedUsername: () => ipcRenderer.invoke('auth:getRememberedUsername'),
  getTables: () => ipcRenderer.invoke('tables:getAll'),
  addTable: (data) => ipcRenderer.invoke('tables:add', data),
  renameTable: (data) => ipcRenderer.invoke('tables:rename', data),
  deleteTable: (data) => ipcRenderer.invoke('tables:delete', data),
  addGameType: (data) => ipcRenderer.invoke('gametypes:add', data),
  updateGameType: (data) => ipcRenderer.invoke('gametypes:update', data),
  deleteGameType: (data) => ipcRenderer.invoke('gametypes:delete', data),
  updateTableStatus: (data) => ipcRenderer.invoke('tables:updateStatus', data),
  startSession: (data) => ipcRenderer.invoke('sessions:start', data),
  endSession: (data) => ipcRenderer.invoke('sessions:end', data),
  getActiveSessions: () => ipcRenderer.invoke('sessions:getActive'),
  getBeverages: () => ipcRenderer.invoke('beverages:getAll'),
  addBeverage: (data) => ipcRenderer.invoke('beverages:add', data),
  updateBeverage: (data) => ipcRenderer.invoke('beverages:update', data),
  deleteBeverage: (data) => ipcRenderer.invoke('beverages:delete', data),
  addOrderToSession: (data) => ipcRenderer.invoke('orders:addToSession', data),
  getOrdersBySession: (data) => ipcRenderer.invoke('orders:getBySession', data),
  getMembers: () => ipcRenderer.invoke('members:getAll'),
  addMember: (data) => ipcRenderer.invoke('members:add', data),
  updateMember: (data) => ipcRenderer.invoke('members:update', data),
  getDailyReport: (data) => ipcRenderer.invoke('reports:getDaily', data),
  getRangeReport: (data) => ipcRenderer.invoke('reports:getRange', data),
  getSettingsTables: () => ipcRenderer.invoke('settings:getTables'),
  updateRate: (data) => ipcRenderer.invoke('settings:updateRate', data),
  getStaff: () => ipcRenderer.invoke('staff:getAll'),
  addStaff: (data) => ipcRenderer.invoke('staff:add', data),
  updateStaff: (data) => ipcRenderer.invoke('staff:update', data),
  deleteStaff: (data) => ipcRenderer.invoke('staff:delete', data),
  getReports: (filter) => ipcRenderer.invoke('reports:get', filter),
  exportSessionsCSV: () => ipcRenderer.invoke('reports:exportSessionsCSV'),
  exportBusinessReportCSV: () => ipcRenderer.invoke('reports:exportBusinessReportCSV'),
  getSettings: () => ipcRenderer.invoke('settings:getAll'),
  saveGeneralSettings: (data) => ipcRenderer.invoke('settings:saveGeneral', data),
  saveTablesPricing: (data) => ipcRenderer.invoke('settings:saveTablesPricing', data),
  createBackup: () => ipcRenderer.invoke('settings:createBackup'),
  restoreBackup: () => ipcRenderer.invoke('settings:restoreBackup'),
  printReceipt: (data) => ipcRenderer.invoke('receipt:print', data),
});
